class Author {
    int author_id;
    String name;
    String bio;
    String profile_image;
    String email;
    String password;
    public Author(int author_id, String name, String bio, String profile_image, String email, String password) {
        this.author_id = author_id;
        this.name = name;
        this.bio = bio;
        this.profile_image = profile_image;
        this.email = email;
        this.password = password;
    }
}
